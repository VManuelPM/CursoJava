package com.amoelcodigo;

public class Calculadora {

    public int sumar(int a, int b) {
        return a + b;
    }

    public float sumar(float x, float y) {
        return x + y;
    }

    public float sumar(int i, float j) {
        return i + j;

    }

    public float sumar(float i, int j) {
        return i + j;
    }

}
